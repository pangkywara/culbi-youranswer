# ─── Helper ────────────────────────────────────────────────────────────────────

def _wkt(lng: float, lat: float) -> str:
    return f"POINT({lng} {lat})"


def _dt(s: str) -> str:
    """Return an ISO 8601 UTC string from 'YYYY-MM-DD[ HH:MM]'."""
    fmt = "%Y-%m-%d %H:%M" if " " in s else "%Y-%m-%d"
    return datetime.strptime(s, fmt).replace(tzinfo=tz.utc).isoformat()


