# ─── CLI ───────────────────────────────────────────────────────────────────────

@click.command()
@click.option("--dry-run", is_flag=True, help="Preview rows without writing to Supabase.")
@click.option("--clear",   is_flag=True, help="Delete all mock-* rows first, then re-seed.")
def main(dry_run: bool, clear: bool) -> None:
    """Seed the Supabase events table with mock ASEAN event data."""

    logger.info("Mock ASEAN event seeder — %d events to insert", len(MOCK_EVENTS))

    client = get_client()

    if clear and not dry_run:
        logger.info("Deleting existing mock rows …")
        resp = client.table("events").delete().like("phq_id", "mock-%").execute()
        logger.info("Deleted mock rows.")

    if dry_run:
        click.echo(f"\n[DRY-RUN] Would upsert {len(MOCK_EVENTS)} events:\n")
        for e in MOCK_EVENTS:
            click.echo(
                f"  {e['phq_id']:<20}  [{e['country']}]  {e['category']:<16}  {e['title'][:60]}"
            )
        click.echo(f"\n  Total: {len(MOCK_EVENTS)} events — nothing written.\n")
        return

    logger.info("Upserting %d mock events …", len(MOCK_EVENTS))
    client.table("events").upsert(MOCK_EVENTS, on_conflict="phq_id").execute()
    logger.info("Done — %d events seeded successfully.", len(MOCK_EVENTS))

    click.echo(f"\n✓ Seeded {len(MOCK_EVENTS)} ASEAN mock events into Supabase.\n")


if __name__ == "__main__":
    main()
